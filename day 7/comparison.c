#include <stdio.h>
int main()
{
    int a = 10, b = 30;
    printf(" A equals B: %d\n", a==b);
    printf(" A not equals B: %d\n", a!=b);
    printf(" A greater than B: %d\n", a>b);
    printf(" A less than B: %d\n", a<b);
    printf(" A greater than or equal to B: %d\n", a>=b);
    printf(" A less than or equal to B: %d\n", a<=b);
    return 0;
}