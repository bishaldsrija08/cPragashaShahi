#include<stdio.h>
int main(){
    // This program demonstrates various output techniques in C.
    printf("Hello World!\n");
    printf("Welcome to C programming.\n");
    printf("This is a simple output program.\n\nThank you for learning C with us!\n");

    // Using escape sequences \t to format the output
    printf("Have a great day!\t Keep coding and keep learning.\n");

    // Using escape sequences to include special characters in the output
    printf("I like \"C\" programming.\n");

    // Using escape sequences to include a backslash in the output
    printf("This is a backslash: \\");
    return 0;
}